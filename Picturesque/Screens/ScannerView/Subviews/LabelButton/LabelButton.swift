import UIKit

class LabelButton: UIButton {

    override init(frame: CGRect) {
        super.init(frame: frame)
        self.commonInit()
    }
    
    required init?(coder aDecoder: NSCoder) {
        super.init(coder: aDecoder)
        self.commonInit()
    }
    
    func commonInit() {
        addDropShadow(withRadius: 4, andOffset: CGSize(width: 0, height: 2))
    }

}
